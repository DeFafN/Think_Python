def gcd(a: int, b: int) -> int:
    """
    Возвращает наибольший общий делитель (НОД) двух чисел по алгоритму Евклида.

    Параметры:
        a (int): первое целое число
        b (int): второе целое число

    Возвращает:
        int: НОД(a, b)

    Примеры:
        gcd(48, 18)
        6
        gcd(17, 13)
        1
    """
    if b == 0:
        return a
    return gcd(b, a % b)

print(gcd(48, 18))