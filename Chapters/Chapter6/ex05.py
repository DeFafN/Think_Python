def is_power(a: int, b: int) -> bool:
    """
    Проверяет, является ли число a степенью числа b (a = b^n для некоторого целого n >= 0).

    Специальные случаи:
    - Если a == 1, возвращаем True, потому что любое число в степени 0 равно 1.
    - Если b == 1, то a должно быть 1 (иначе это не степень).
    - Если b == 0, то a должно быть 0 (для a > 0 — только 0^0 неопределено, но по условию a и b неотрицательные).
    - Если a < b и a != 1, то a не может быть степенью b.

    Возвращает:
        bool: True, если a является степенью b, иначе False

    Примеры:
        >>> is_power(8, 2)
        True
        >>> is_power(27, 3)
        True
        >>> is_power(10, 2)
        False
        >>> is_power(1, 5)
        True

    """
    # Базовые случаи
    if a == 1:
        return True  # b^0 = 1 для любого b ≠ 0; если b == 0, то 0^0 — условно True
    if b == 0:
        return a == 0  # 0^n = 0 при n > 0; 0^0 = ? → условно считаем, что 0 — степень 0
    if b == 1:
        return a == 1  # 1^n = 1, так что только a=1 подходит
    if a == 0:
        return False  # если b > 1, то b^n никогда не будет 0
    if a < b:
        return False  # a не может быть степенью b, если меньше b и не равен 1

    # Рекурсивный случай
    if a % b != 0:
        return False  # если не делится нацело — не степень
    return is_power(a // b, b)