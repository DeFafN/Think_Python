def any_lowercase1(string):
    """
    Проверяет, содержит ли строка хотя бы один символ в нижнем регистре.
    Проходит по каждому символу строки. Возвращает True при первом встретившемся
    символе в нижнем регистре. Если таких символов нет, возвращает False.

    Параметры:
        string (str): строка для проверки

    Возвращает:
        bool: True, если найден хотя бы один символ в нижнем регистре, иначе False
    """
    for letter in string:
        if letter.islower():
            return True
    else:
        return False

def any_lowercase2(string):
    """
    НЕПРАВИЛЬНАЯ функция: всегда возвращает строку 'True'.

    Содержит ошибку: проверяет не символ c, а строку 'c', которая всегда в нижнем регистре.
    Возвращает строку ('True' или 'False'), а не булево значение.

    Параметры:
        s (str): строка для проверки (игнорируется по сути)

    Возвращает:
        str: всегда возвращает 'True'
    """
    for c in string:
        if 'c'.islower():
            return 'True'
    else:
        return 'False'

def any_lowercase3(s):
    """
    НЕПРАВИЛЬНАЯ функция: возвращает результат проверки последнего символа строки.

    Переменная flag перезаписывается на каждом символе. В итоге возвращается
    результат вызова any_lowercase3() только для последнего символа.

    Параметры:
        s (str): строка для проверки

    Возвращает:
        bool: True, если последний символ в нижнем регистре, иначе False
    """
    for c in s:
        flag = c.islower()
    return flag

def any_lowercase4(s):
    """
    Определяет, содержит ли строка хотя бы один символ в нижнем регистре.

    Использует логическое ИЛИ для накопления результата. Если хотя бы один символ
    в нижнем регистре, flag станет True и останется таким до конца.

    Параметры:
        s (str): строка для проверки

    Возвращает:
        bool: True, если в строке есть хотя бы один символ в нижнем регистре, иначе False
    """
    flag = False
    for c in s:
        flag = flag or c.islower()
    return flag

def any_lowercase5(s):
    """
    Возвращает False при первом же символе, который не в нижнем регистре.
    Отрицание: функция проверяет, что строка НЕ содержит заглавных символов.

    Параметры:
        s (str): строка для проверки

    Возвращает:
        bool: True, если все символы в нижнем регистре, иначе False
    """
    for c in s:
        if not c.islower():
            return False
    return True

print(any_lowercase4('Банан'))